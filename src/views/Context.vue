<p><em>【注意: 关于React元素】</em></p>
<p><em>0. 所有的标签都必需闭合。即使单标签也要闭合, 如果 img 标签 要写成 <code>&lt;img/&gt;</code></em></p>
<p><em>1. DOM 元素属性应该小写，但是在 React 元素中，元素的属性需要写在【驼峰的形式】, 如果 autofocus 要写在 autoFocus, 如果 readonly 要写在 readOnly</em></p>
<p><em>2. 所有的元素的属性必须是下面地址中所包含的标签, 没有包含的不允许使用</em></p>
<p><a href="https://reactjs.org/docs/dom-elements.html" rel="nofollow">https://reactjs.org/docs/dom-elements.html</a></p>
